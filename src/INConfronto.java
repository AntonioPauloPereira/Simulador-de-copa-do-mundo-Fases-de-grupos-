    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        // TODO add your handling code here:
        try {
        // Varre todas as linhas da tabela que estão aparecendo na tela
        for (int i = 0; i < modeloTabela.getRowCount(); i++) {
            String nomeSelecao = (String) modeloTabela.getValueAt(i, 0);
            
            // Pega o que o usuário digitou em cada coluna e converte para número inteiro
            int pts = Integer.parseInt(modeloTabela.getValueAt(i, 1).toString());
            int pj  = Integer.parseInt(modeloTabela.getValueAt(i, 2).toString());
            int vit = Integer.parseInt(modeloTabela.getValueAt(i, 3).toString());
            int e   = Integer.parseInt(modeloTabela.getValueAt(i, 4).toString());
            int der = Integer.parseInt(modeloTabela.getValueAt(i, 5).toString());
            int gm  = Integer.parseInt(modeloTabela.getValueAt(i, 6).toString());
            int gc  = Integer.parseInt(modeloTabela.getValueAt(i, 7).toString());
            int sg  = Integer.parseInt(modeloTabela.getValueAt(i, 8).toString());
            
            // Localiza a seleção na memória do sistema e atualiza seus dados
            Selecoes sel = buscarSelecaoPorNome(nomeSelecao);
            if (sel != null) {
                sel.setPontos(pts);
                sel.setPartidasJogadas(pj);
                sel.setVitorias(vit);
                sel.setEmpates(e);
                sel.setDerrotas(der);
                sel.setGolsMarcados(gm);
                sel.setGolContra(gc);
                sel.setSaldoGols(sg);
            }
        }
        
        javax.swing.JOptionPane.showMessageDialog(this, "Classificação do grupo salva com sucesso!");
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar. Certifique-se de digitar apenas números nas tabelas.");
    }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jcboxGruposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcboxGruposActionPerformed
                                               
    int indexSelecionado = jcboxGrupos.getSelectedIndex();
    
    if (indexSelecionado != -1) {
        Grupo grupoAtual = GerenciadorDados.todosGrupos.get(indexSelecionado);
        modeloTabela.setRowCount(0); // Limpa a tabela antes de carregar
        
        for (String nomeSelecao : grupoAtual.getSelecoes()) {
            // Busca o objeto completo da seleção para pegar os dados reais do campeonato
            Selecoes sel = buscarSelecaoPorNome(nomeSelecao);
            
            if (sel != null) {
                // Adiciona a linha com os dados REAIS armazenados na seleção
                modeloTabela.addRow(new Object[]{
                    sel.getNome(), 
                    sel.getPontos(), 
                    sel.getPartidasJogadas(), 
                    sel.getVitorias(), 
                    sel.getEmpates(), 
                    sel.getDerrotas(), 
                    sel.getGolsMarcados(), 
                    sel.getGolContra(), 
                    sel.getSaldoGols()
                });
            } else {
                // Caso de segurança (se não achar o objeto, põe zerado)
                modeloTabela.addRow(new Object[]{nomeSelecao, 0, 0, 0, 0, 0, 0, 0, 0});
            }
        }
    }

    }//GEN-LAST:event_jcboxGruposActionPerformed

