    private Selecoes buscarSelecaoPorNome(String nome) {
    for (Selecoes s : GerenciadorDados.todasSelecoes) {
        // Substitua por getNome() ou o método que você usa para pegar o nome da seleção
        if (s.getNome().equalsIgnoreCase(nome)) { 
            return s;
        }
    }
    return null;
}
    
